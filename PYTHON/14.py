# SIMPLE CODE USING CODITIONAL STATEMENT USING PYTHON PROGRAMMING LANGUAGE :
marks = int(input("Enter your marks: "))

if marks < 0 or marks > 100: # USED IF STATEMENT AND USED LOGICAL OPERATOR TO MAKE CODE EFFIECIENT .
    print("Invalid marks")
elif marks >= 90:          # FIXED A GRADE TO GIVE THE STUDENTS WHO SCORED 905
    print("Grade A+ - Topper mindset")
elif marks >= 75:           # THE STUDENT WHO GOT AVERAGE  GIVEN GRADE `A 
    print("Grade A - Strong work")
elif marks >= 50:            # THE STUDENT WHO GOT 50 / DESCENT SCORE GIVEN GRADE B 
    print("Grade B - Decent, push harder")
else:                       # AT THE END THE STUDENT WHO GOT LESS SCORE WILL BE FAILED USING ELSE :
    print("Fail - Reality check")

