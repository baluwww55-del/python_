# fundamentals: 
# variables : something that stores a value and is changeable.
# data types : different kinds of values that can be stored in variables.
# constants : something that stores a value and is unchangeable.
# operators : symbols that perform operations on variables and values.


# simple code example  for variables.
a = 10
b = 20
print(a)
# if we put string the output will be a 
a = 10
b = 20
print("a")

# what are data types ?
# data types are different kinds of values that can be stored in variables.
# commom data types in python are :
# int : integer values (whole numbers)
# float : decimal values (numbers with a fractional part)
# str : string values (text)
# bool : boolean values (True or False)

# example code for data types.
x = 5           # int
y = 3.14        # float
name = "Balaji"  # str
is_student = True  # bool       
print(type(is_student))

# constants in python.
# in python there are no built in constants but we can use uppercase variable names to indicate that a variable is a constant and should not be changed.


# operators in python.
# arithmetic operators : +, -, *, /, %, **, //
# comparison operators : ==, !=, >, <, >=, <= 
# logical operators : and, or, not
# example code for operators.
a = 10  
b = 5
print(a + b)  # addition
print(a > b)  # comparison  
print(a > 5 and b < 10)  # logical

