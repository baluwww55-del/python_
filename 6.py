# REPITITION:
message = " Balu works very hard. "
print(message *5)
# repitition means to repeat the same string multiple times.

# METHODS :
message = " Balu works very hard. "
print(message.lower())
# lower() method converts all the characters in the string to lowercase.
message = " Balu works very hard. "
print(message.upper())
# upper() method converts all the characters in the string to uppercase.

# STRIP :
message = "  Balu works very hard.  "
print(message.strip()*2) 
# strip() method removes any leading (spaces at the beginning) and trailing (spaces at the ending).

# REPLACE :
message = " Balu works very hard. "
print(message.replace("Balu","ramu"))

